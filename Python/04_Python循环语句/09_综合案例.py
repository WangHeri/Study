"""
某公司,账户余额有1W元,给20名员工发工资。
员工编号从1到20,从编号1开始,依次领取工资,每人可领取1000元
领工资时,财务判断员工的绩效分(1-10)(随机生成),如果低于5,不发工资，换下一位
如果工资发完了，结束发工资。
提示：
continue用于跳过员工,break直接结束发工资
if判断余额,不要忘记发完工资后,余额减少1000哦
"""
import random
i = 10000
for x in range(1,21):
    j = random.randint(1,11)
    if j < 5:
        print(f"编号为{x}的员工,绩效为{j},不发工资,下一位")
        continue
    else:
        i = i - 1000
        print(f"编号为{x}的员工,绩效为{j},发工资1000,账户余额{i}")
        if i == 0:
            print("工资已发放完毕，下个月在领取")
            break

      